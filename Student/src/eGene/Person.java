package eGene;

/**
 * 人员类
 */
public class Person {

	//编号
	private String	id;
	//姓名
	private String	name;

	//性别
	private String 	sex;


	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}
}
